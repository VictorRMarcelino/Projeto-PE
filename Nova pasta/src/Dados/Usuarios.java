    private void confirmarAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarAtualizarActionPerformed
        try {
            String url = "jdbc:mysql://localhost:3306/funcionários";
            String user = "root";
            String pass = "";

            Connection conn = DriverManager.getConnection(url, user, pass);
            Statement stmt = conn.createStatement();
            String Query = "UPDATE funcionários SET Usuário = '" + usuarioAtualizar.getText() + "',Senha = '" + senhaAtualizar.getText() + "',Cpf = '" + cpfAtualizar.getText() + "' WHERE Cpf = '" + cpfAtualizar.getText() + "'";
            stmt.execute(Query);

            JOptionPane.showMessageDialog(null, "Atualizado!");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Falha ao atualizar funcionários \n"
                    + "Erro: "+e.toString());
        }
        filtro();
        Usu();
    }//GEN-LAST:event_confirmarAtualizarActionPerformed

