    final void Usu() {
        try {
            String url = "jdbc:mysql://localhost:3306/funcionários";
            String user = "root";
            String pass = "";

            Connection conn = DriverManager.getConnection(url, user, pass);
            Statement stmt = conn.createStatement();
            String Query = "SELECT * FROM funcionários";
            ResultSet Result = stmt.executeQuery(Query);
            DefaultListModel list = new DefaultListModel();
            while (Result.next()) {
                list.addElement("");
                list.addElement("Usuário: "+Result.getString(1));
                list.addElement("Cpf: "+Result.getString(3));
                list.addElement("");
                list.addElement("");
                list.addElement("");
            }
            ListaFuncionarios.setModel(list);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Falha ao carregar lista do histórico\n"
                    +"Erro: "+e.toString());
        }
    }
