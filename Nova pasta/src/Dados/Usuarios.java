    private void ListaAtualizarValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ListaAtualizarValueChanged
        try {
            String url = "jdbc:mysql://localhost:3306/funcionários";
            String user = "root";
            String pass = "";

            Connection conn = DriverManager.getConnection(url, user, pass);
            Statement stmt = conn.createStatement();
            String Query = "SELECT * FROM funcionários WHERE Cpf = '" + ListaAtualizar.getSelectedValue() + "';";
            ResultSet Result = stmt.executeQuery(Query);
            DefaultListModel list = new DefaultListModel();
            while (Result.next()) {
                usuarioAtualizar.setText(Result.getString(1));
                senhaAtualizar.setText(Result.getString(2));
                cpfAtualizar.setText(Result.getString(3));
            }
            ListaAtualizar.setModel(list);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro: "+e.toString());
        }
        filtro();
        Usu();
    }//GEN-LAST:event_ListaAtualizarValueChanged

