    private void ConfirmarExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarExcluirActionPerformed
        if (CpfExcluir.getText().isEmpty() || SenhaGerenteExcluir.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Usuário e/ou Senha do Gerente Vazios,Complete as Informações, Por Favor!", "Alert", JOptionPane.WARNING_MESSAGE);
        } else {
            if (SenhaGerenteExcluir.getText().equals("2307")) {
                try {
                    String url = "jdbc:mysql://localhost:3306/funcionários";
                    String user = "root";
                    String pass = "";

                    Connection conn = DriverManager.getConnection(url, user, pass);
                    Statement stmt = conn.createStatement();
                    String Query = "DELETE FROM `funcionários` WHERE Cpf = '" + CpfExcluir.getText() + "'";
                    stmt.execute(Query);

                    JOptionPane.showMessageDialog(null, "Funcionário Deletado Com Sucesso!");

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Falha ao excluir funcionário\n"
                            +"Erro: "+e.toString());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Senha do gerente está incorreta");
            }
        }
        filtro();
        Usu();
    }//GEN-LAST:event_ConfirmarExcluirActionPerformed

