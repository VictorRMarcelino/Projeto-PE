    private void ConfirmarAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarAdicionarActionPerformed
        if (UsuarioAdicionar.getText().isEmpty() || SenhaAdicionar.getText().isEmpty() || cpfAdicionar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Usuário, Senha ou CPF Vazios,Complete as Informações, Por Favor!", "Alert", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                String url = "jdbc:mysql://localhost:3306/funcionários";
                String user = "root";
                String pass = "";

                Connection conn = DriverManager.getConnection(url, user, pass);
                Statement stmt = conn.createStatement();
                String Query = "INSERT INTO `funcionários` (`Usuário`,`Senha`, `Cpf`) VALUES ('" + UsuarioAdicionar.getText() + "','" + SenhaAdicionar.getText() + "','" + cpfAdicionar.getText() + "')";
                stmt.execute(Query);
                JOptionPane.showMessageDialog(null, "Funcionário adicionado com sucesso!");

                UsuarioAdicionar.setText(null);
                SenhaAdicionar.setText(null);
                cpfAdicionar.setText(null);

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Falha ao adicionar funcionário\n"
                        +"Erro: "+e.toString());
            }
        }
        filtro();
        Usu();
    }//GEN-LAST:event_ConfirmarAdicionarActionPerformed

